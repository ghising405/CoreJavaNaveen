package JavaBasics;

/*
final = keyword
1. it is used to define constant values. That means values that cannot be changed. Cannot modify the value of final variable
2. it is also used to prevent inheritance.
--> created package = test package; class= parentClass19;
3. it is also used to prevent method overriding. If you don't want to share some of your method, you can make that method final so that child cannot get access.


 */

public class FinalConcept19 {

    public static void main(String[] args) {

        final int i = 10; //final is used to define constant values. Values that cannot be changed.

    }


}
